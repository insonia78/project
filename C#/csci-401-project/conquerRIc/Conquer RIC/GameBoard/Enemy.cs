using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace GameBoard
{
    class Enemy : Character
    {

        public Enemy()
        {
            row = 0;
            col = 0;
        }

        public Enemy (int r, int c, int charSpeed)
        {
            row = r;
            col = c;
            hp = 1; 
            moveSpeed = charSpeed;
        }
    }
}
